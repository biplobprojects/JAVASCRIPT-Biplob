// NonPrimitive datatypes are used to hold multiple values:

// ARRAY JS=[]:
var size=[38,13,45,67];  


// to read the data:
console.log(size); 

//  to find the type of datatype:
console.log(typeof size)    



//OBJECT JS={}:
var employee={
    id : 123,
    name:"biplob",
    salary:78000
}

//Read the data:
console.log(employee);

//Type of datatype:
console.log(typeof employee);

